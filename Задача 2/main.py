def первичная_обработка_логов(логи):
  новые_логи = []

  for строка in логи:
      # Удаляем строки, начинающиеся с "####"
      if not строка.startswith("####"):
          # Удаляем "%%" в начале строки
          if строка.startswith("%%"):
              строка = строка[2:]
          новые_логи.append(строка.strip())

  return новые_логи

# Считываем логи с клавиатуры (может быть заменено на чтение из файла)
входные_логи = []
while True:
  строка = input()
  if not строка:
      break
  входные_логи.append(строка)

# Проводим первичную обработку логов
обработанные_логи = первичная_обработка_логов(входные_логи)

# Выводим результат
for строка in обработанные_логи:
  print(строка)
